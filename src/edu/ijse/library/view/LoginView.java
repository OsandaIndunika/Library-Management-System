    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if(jUserName.getText().equals("")){
            JOptionPane.showMessageDialog(null, "PLEASE FILL THE USERNAME");
        }else if(jPassword.getText().equals("")){
            JOptionPane.showMessageDialog(null, "PLEASE FILL THE PASSWORD");
        }else if(jUserName.getText().contains("admin") && jPassword.getText().contains("admin")){
            JOptionPane.showMessageDialog(null, "LOGIN SUCESSFULL");
        }
        else{
            JOptionPane.showMessageDialog(null, "WRONG USERNAME OR PASSWORD", "Message", JOptionPane.ERROR_MESSAGE);
        }
        try {
                new DashboardView().setVisible(true);
            } catch (Exception ex) {
                Logger.getLogger(LoginView.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btnLoginActionPerformed

    private void jShowPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jShowPasswordActionPerformed
        if(jShowPassword.isSelected()){
            jPassword.setEchoChar((char)0);
        }else{
            jPassword.setEchoChar('*');
        }
    }//GEN-LAST:event_jShowPasswordActionPerformed

