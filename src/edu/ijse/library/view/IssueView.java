    public void loadTable() throws Exception{
        String columns[] = {"IssueID", "MemberID", "BookCode", "IssueDate", "DueDate"};
        DefaultTableModel dtm=new DefaultTableModel(columns,0){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        tblIssue.setModel(dtm);
    }
    
    private void clearData(){
        txtIssueID.setText("");
        txtBookCode.setText("");
        txtMemberID.setText("");
        jIssueDate.setDate(null);
        jDueDate.setDate(null);
    }

    private void Issue() {
        try {
            IssueDto dto = new IssueDto();
            dto.setIssueId(txtIssueID.getText());
            dto.setBookCode(txtBookCode.getText());
            dto.setMemberId(txtMemberID.getText());
            dto.setIssueDate(jIssueDate.getDate());
            dto.setDueDate(jDueDate.getDate());

            issueDtos.add(dto);

            Object rowData[] = {dto.getIssueId(), dto.getBookCode(), dto.getMemberId(), dto.getIssueDate(), dto.getDueDate()};
            DefaultTableModel dtm = (DefaultTableModel) tblIssue.getModel();
            dtm.addRow(rowData);

            String rsp = Issue_Controller.Issue(dto);
            JOptionPane.showMessageDialog(this, rsp);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        clearData();
    }

    private void searchBook() {
        try {
            String bookCode = txtBookCode.getText();
             BookDto bookDto = Book_Controller.get(bookCode);
            if(bookDto != null){
                lblBookData.setText(bookDto.getBookTitle() + "|" + bookDto.getPublisher() + " | " + bookDto.getAuthor());
            } else {
                lblBookData.setText("Book Not Found.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    private void searchMember() {
         try {
            String memberID = txtMemberID.getText();
            MemberDto memberDto = Member_Controller.get(memberID);
            if(memberDto != null){
                lblMemberData.setText(memberDto.getMemberTitle() + memberDto.getMemberName() + " | " + memberDto.getContact() + " | " + memberDto.getEmail());
            } else {
                lblMemberData.setText("Member Not Found.");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    
